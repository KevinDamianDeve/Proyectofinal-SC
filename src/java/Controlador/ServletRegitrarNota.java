    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
            
       int coda = Integer.parseInt(request.getParameter("alumno"));
       int c = Integer.parseInt(request.getParameter("curso"));
        int tri = Integer.parseInt(request.getParameter("trimestre"));
     float not =  Float.parseFloat(request.getParameter("nota"));
    String obs =  request.getParameter("obs");            

        NOTAS f = new NOTAS();
        
        f.setIdalumno(coda);
        f.setIdcurso(c);
        f.setIdtrimestre(tri);
        f.setNota(not);
        f.setObservacion(obs);
        if (NOTAS.RegistrarNotas(f)) {
            
          request.setAttribute("mensaje","LA NOTA SE INGRESO DE MANERA CORRECTA");
        }else{
            request.setAttribute("mensaje","ERROR LA NOTA NO SE INGRESO");
        }
        request.getRequestDispatcher("Listanotas.jsp").forward(request, response);
        processRequest(request, response);
    }

